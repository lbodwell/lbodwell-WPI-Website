/*
PS.init( system, options )
Called once after engine is initialized but before event-polling begins.
This function doesn't have to do anything, although initializing the grid dimensions with PS.gridSize() is recommended.
If PS.grid() is not called, the default grid dimensions (8 x 8 beads) are applied.
Any value returned is ignored.
[system : Object] = A JavaScript object containing engine and host platform information properties; see API documentation for details.
[options : Object] = A JavaScript object with optional data properties; see API documentation for details.
*/

PS.init = function(system, options) {
	"use strict"; // Do not remove this directive!

	// Establish grid dimensions
	PS.gridSize(15, 15 );
	// Set background color to sky blue
	PS.gridColor(56, 166, 209);

	// Set shadow to white to give off glowing effect
	PS.gridShadow(PS.COLOR_WHITE);
	for (let x = 0; x < 15; x++) {
		for (let y = 0; y < 15; y++) {
			PS.color( x, y, PS.COLOR_BLACK);
		}
	}
	// Remove borders of all beads
	PS.border(PS.ALL, PS.ALL, 0);
	
	// Change status line color and text
	PS.statusColor(PS.COLOR_BLACK);
	PS.statusText("Rainbow");
	
	// Preload click sound
	PS.audioLoad("fx_drip2");
	PS.audioLoad("fx_pop");
};

/*
PS.touch ( x, y, data, options )
Called when the left mouse button is clicked over bead(x, y), or when bead(x, y) is touched.
This function doesn't have to do anything. Any value returned is ignored.
[x : Number] = zero-based x-position (column) of the bead on the grid.
[y : Number] = zero-based y-position (row) of the bead on the grid.
[data : *] = The JavaScript value previously associated with bead(x, y) using PS.data(); default = 0.
[options : Object] = A JavaScript object with optional data properties; see API documentation for details.
*/

PS.touch = function(x, y, data, options) {
	"use strict"; // Do not remove this directive!

	var next;
	var colorName;
	var playSound = true;

	// Determine the new color of the selected bead
	if (PS.color(x, y, PS.current) === PS.COLOR_BLACK) {
		if (y === 0 || x === 0 || x === 14) {
			next = PS.COLOR_RED;
			colorName = "Red";
		} else if (y === 1 || x === 1 || x === 13) {
			next = PS.COLOR_ORANGE;
			colorName = "Orange";
		} else if (y === 2 || x === 2 || x === 12) {
			next = PS.COLOR_YELLOW;
			colorName = "Yellow";
		} else if (y === 3 || x === 3 || x === 11) {
			next = PS.COLOR_GREEN;
			colorName = "Green";
		} else if (y === 4 || x === 4 || x === 10) {
			next = PS.COLOR_BLUE;
			colorName = "Blue";
		} else if (y === 5 || x === 5 || x === 9) {
			next = PS.COLOR_INDIGO;
			colorName = "Indigo";
		} else if (y === 6 || x === 6 || x === 8) {
			next = PS.COLOR_VIOLET;
			colorName = "Violet"
		} else {
			// Set the beads that aren't part of the rainbow to invisible and don't play a sound on click
			PS.visible(x, y, false);
			playSound = false;
		}
		// Set the status text and color to the new color of the bead
		PS.statusColor(next);
		PS.statusText(colorName);
	
		// Set the bead's new color
		PS.color(x, y, next);
		
		if (playSound) {
			// Generate a random whole number between 0 and 1 to determine which sound to play
			var rand = Math.floor(Math.random() * 2);
			var sound = (rand === 0) ? "fx_drip2" : "fx_pop";

			// Play the determined sound
			PS.audioPlay(sound);
		}		
	}
};

